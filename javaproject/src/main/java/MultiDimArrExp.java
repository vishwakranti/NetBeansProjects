 
public class MultiDimArrExp {
   
    public static void main(String args[]){
        //Declaring and initializing 2D array
        int numArr[][]={{1,2,3},{4,5,6},{7,8,9}};
        
        //Printing 2D Array
        for(int i=0;i<3;i++)
         {
            for(int j=0;j<3;j++)
         {
                System.out.print(numArr[i][j]+" ");
         }
         System.out.println();

        }
}
}
    
