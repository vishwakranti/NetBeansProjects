public class SwitchStmtExp {
    public static void main(String[] args){
        int choice = 3;
        switch(choice){
            case 1:
                System.out.println("The number is one");
                break;
                
            case 2:
               System.out.println("The number is two");
               break;
               
            case 3:
                System.out.println("The number is three");
                break;
                
            default:
                System.out.println("The choice does not match any of the case blocks");
        }
    }
    
}
